    private void zetasKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_zetasKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) {
            evt.consume();
        }       
    }//GEN-LAST:event_zetasKeyTyped

